 <div class="container py-5">
     <p>The Monkey Weather or "O Tempo Macaco" in portuguese is a meterologic service system in which the profit
         of the same goes to Monkey Santuaries and Non-profit Animal Wellfare Organizations.</p>
     <p>Created to provide information about the weather whilst also sharing awareness about the dificulties
         monkeys worldwide are facing everyday.</p>

 </div>
 <div class="container py-3">
     <h2>Did you know?</h2>
     <p>More than half the world's primates, including apes, lemurs and monkeys, are facing extinction. The main
         threats are well-known which include habitat loss especially the clearing of tropical forests, and the
         hunting of primates for food and the illegal wildlife trade.</p>
